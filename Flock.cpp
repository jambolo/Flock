Flock::~Flock()
{
}


/********************************************************************************************************************/
/*																													*/
/*																													*/
/********************************************************************************************************************/

void Flock::Update( float dt, HeightField const & terrain, float xyScale, float seaLevel )
{
	for ( iterator ppB = begin(); ppB != end(); ++ppB )
	{
		( *ppB )->Update( dt, *this, terrain, xyScale, seaLevel );
	}
}
